public class Operations {

    public static void main(String[] args) {
        int a = 10;
        int b = 12;

        int sum = a + b;
        System.out.println(sum);

        int diff = a - b;
        System.out.println(diff);

        int mult = a * b;
        System.out.println(mult);

        float div = a / b;
        System.out.println(div);


    }
}
